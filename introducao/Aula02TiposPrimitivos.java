package academy.devdojo.maratonajava.introducao;

public class Aula02TiposPrimitivos {
    public static void main (String[] args){

        int age = 40;
        System.out.println("idade: "+age+" anos");

        long numberBig = 1000000000000L;
        System.out.println("Número grande: "+numberBig);

        double paymentBig = 20400000000.04D;
        System.out.println("Pagamento grande: "+paymentBig);

        boolean status1 = false;
        System.out.println("Status 1:"+status1);

        boolean status2 = true;
        System.out.println("Status 2:"+status2);

        String name = "João Pedro Braga";
        System.out.println("Meu nome é: "+name);

    }
}
